'''Question: 
Write a Python program to iterate over dictionaries using for loops.
'''

# Python code: 


d = {'x': 10, 'y': 20, 'z': 30} 
for dict_key, dict_value in d.items():
    print(dict_key,'->',dict_value)
	

'''Output sample: 
y -> 20                                                                                                       
z -> 30                                                                                                       
x -> 10 
'''