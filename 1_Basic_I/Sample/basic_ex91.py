'''Question: 
Write a Python program to swap two variables.
'''

# Python code: 

a = 30
b = 20
print("\nBefore swap a = %d and b = %d" %(a, b))
a, b = b, a
print("\nAfter swaping a = %d and b = %d" %(a, b))
print()


'''Output sample: 
Before swap a = 30 and b = 20                                                                                 
                                                                                                              
After swaping a = 20 and b = 30
'''