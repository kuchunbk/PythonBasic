'''Question: 
Write a Python program to check if a number is positive, negative or zero.
'''

# Python code: 

num = float(input("Input a number: "))
if num > 0:
   print("It is positive number")
elif num == 0:
   print("It is Zero")
else:
   print("It is a negative number")
   

