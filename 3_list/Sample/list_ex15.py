'''Question: 
Write a Python program to shuffle and print a specified list. 
'''

# Python code: 

from random import shuffle
color = ['Red', 'Green', 'White', 'Black', 'Pink', 'Yellow']
shuffle(color)
print(color)


'''Output sample: 
['Yellow', 'Pink', 'Green', 'Red', 'Black', 'White'] 
'''