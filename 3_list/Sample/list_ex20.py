'''Question: 
Write a Python program access the index of a list.
'''

# Python code: 

nums = [5, 15, 35, 8, 98]
for num_index, num_val in enumerate(nums):
    print(num_index, num_val)
	

'''Output sample: 
0 5                                                                                                           
1 15                                                                                                          
2 35                                                                                                          
3 8                                                                                                           
4 98
'''