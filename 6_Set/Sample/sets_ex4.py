'''Question: 
Write a Python program to remove item(s) from set.
'''

# Python code: 

num_set = set([0, 1, 3, 4, 5])
num_set.pop()
print(num_set)
num_set.pop()
print(num_set)


'''Output sample: 
{1, 3, 4, 5}                                                                                                  
{3, 4, 5} 
'''